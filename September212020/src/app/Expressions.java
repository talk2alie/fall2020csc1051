package app;

public class Expressions {
    public static void main(String[] args) {
        System.out.println();

        // Expressions evaluate to a value
        // The value can be of any data type
        // E.g. 2 + 2 evaluates to 4; so this is an integer expression
        //      2.0 * 4 evaluates to 8.0; so this is a floating point expression
        // An expression that evaluates to a Boolean value; true or false, is a Boolean 
        // expression, or what we call a condition
        //
        // So a conditional, in Java, is any statement that selects what statement to run 
        // based on the value of a Booean expression or condition. If the value is true, one 
        // set of statements run and if the value if false, another set run

        System.out.println();
    }
}
